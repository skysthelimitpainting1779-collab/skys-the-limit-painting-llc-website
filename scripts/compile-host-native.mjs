#!/usr/bin/env node
/**
 * Compile .agents/specialists.json + .agents/skills → host-native layouts.
 *
 * Targets:
 *   Claude Code   .claude/agents/*.md  .claude/skills/*
 *   Cursor        .cursor/agents/*.md  .cursor/rules/specialist-*.mdc
 *   Codex         .codex/agents/*.toml  (skills already .agents/skills)
 *   Antigravity   .agents/rules (keep)  .agents/skills (SSOT)
 *   Copilot       .github/skills/*  .github/copilot-instructions.md
 *   Gemini CLI    GEMINI.md + .gemini/settings.json context.fileName
 *
 *   npm run host:compile
 *   npm run host:compile -- --clean-domains
 */
import {
  existsSync,
  mkdirSync,
  readFileSync,
  writeFileSync,
  readdirSync,
  rmSync,
  cpSync,
  statSync,
} from 'node:fs';
import { join, dirname } from 'node:path';

const ROOT = process.cwd();
const SPECS = join(ROOT, '.agents', 'specialists.json');
const SKILLS_SSOT = join(ROOT, '.agents', 'skills');
const cleanDomains = process.argv.includes('--clean-domains');

function ensureDir(p) {
  mkdirSync(p, { recursive: true });
}

function write(rel, content) {
  const p = join(ROOT, rel);
  ensureDir(dirname(p));
  writeFileSync(p, content, 'utf8');
  return rel;
}

function loadSpecs() {
  if (!existsSync(SPECS)) throw new Error(`Missing ${SPECS}`);
  return JSON.parse(readFileSync(SPECS, 'utf8'));
}

function claudeAgent(a) {
  const skills = (a.skills || [])
    .map((s) => `- \`.agents/skills/${s}/SKILL.md\` (mirrored to \`.claude/skills/${s}/\`)`)
    .join('\n');
  return `---
name: ${a.id}
description: ${JSON.stringify(a.description).slice(1, -1)}
tools: [${(a.tools || ['Read', 'Write', 'Edit', 'Grep', 'Bash', 'Glob']).join(', ')}]
model: ${a.model || 'sonnet'}
permissionMode: default
---

# ${a.name}

${a.prompt}

## Jurisdiction (write only)

**Allow:** ${(a.allow_globs || []).map((g) => `\`${g}\``).join(', ')}

**Deny:** ${(a.deny_globs || []).map((g) => `\`${g}\``).join(', ')}

Outside allow → stop and hand off to the owning specialist.

## Skills (load on match)

${skills || '_None listed — follow AGENTS.md_'}

## Verify

\`\`\`bash
npm run lint
npm test
# or
npm run goal:verify
\`\`\`

Obey root AGENTS.md (Karpathy + RPI + no dumps).
`;
}

function cursorAgent(a) {
  return `---
name: ${a.id}
description: ${a.description}
---

# ${a.name}

${a.prompt}

## Write only

Allow: ${(a.allow_globs || []).join(', ')}

Deny: ${(a.deny_globs || []).join(', ')}

Skills: ${(a.skills || []).map((s) => `.agents/skills/${s}/SKILL.md`).join(', ') || 'none'}

Follow root AGENTS.md.
`;
}

function cursorRule(a) {
  const globs = (a.allow_globs || []).join(', ');
  return `---
description: Specialist ${a.id} — auto when matching paths
globs: "${globs}"
alwaysApply: false
---

# ${a.name} (${a.id})

${a.description}

${a.prompt}

**Allow writes:** ${globs || 'n/a'}

**Deny:** ${(a.deny_globs || []).join(', ') || 'n/a'}

Prefer this specialist's jurisdiction. Root kernel: AGENTS.md.
`;
}

function codexToml(a) {
  const instructions = [
    a.prompt,
    '',
    'Jurisdiction allow: ' + (a.allow_globs || []).join(', '),
    'Jurisdiction deny: ' + (a.deny_globs || []).join(', '),
    'Skills: ' + ((a.skills || []).join(', ') || 'none'),
    'Follow repo root AGENTS.md.',
  ].join('\n');
  // Escape for TOML multiline
  const esc = instructions.replace(/\\/g, '\\\\');
  return `# AUTO-GENERATED by npm run host:compile — edit .agents/specialists.json
name = ${JSON.stringify(a.id)}
description = ${JSON.stringify(a.description)}
instructions = """
${esc}
"""
`;
}

function mirrorSkills() {
  const written = [];
  if (!existsSync(SKILLS_SSOT)) return written;
  const targets = [
    join(ROOT, '.claude', 'skills'),
    join(ROOT, '.github', 'skills'),
  ];
  for (const t of targets) {
    ensureDir(t);
  }
  for (const name of readdirSync(SKILLS_SSOT, { withFileTypes: true })) {
    if (!name.isDirectory()) continue;
    const src = join(SKILLS_SSOT, name.name);
    if (!existsSync(join(src, 'SKILL.md'))) continue;
    for (const t of targets) {
      const dest = join(t, name.name);
      if (existsSync(dest)) rmSync(dest, { recursive: true, force: true });
      cpSync(src, dest, { recursive: true });
      written.push(dest.replace(ROOT + '\\', '').replace(ROOT + '/', ''));
    }
  }
  return written;
}

function copilotInstructions() {
  return `<!-- AUTO-GENERATED by npm run host:compile — SSOT is root AGENTS.md -->
# GitHub Copilot

Follow **AGENTS.md** at the repository root (portable kernel).

## Specialists

Use path-appropriate specialists (see \`.agents/specialists.json\`). Skills live under \`.agents/skills/\` and are mirrored to \`.github/skills/\`.

## Commands

\`\`\`bash
npm run lint
npm test
npm run build
npm run graph:query -- "<task>"
npm run goal:verify
npm run host:compile
\`\`\`

## Never

- Bulk-load graphify-out/wiki or GRAPH_REPORT.md
- Soft-skip failing checks as a "fix"
- Emojis in product \`src/\`
`;
}

function geminiSettingsPatch() {
  const p = join(ROOT, '.gemini', 'settings.json');
  let j = {};
  if (existsSync(p)) {
    try {
      j = JSON.parse(readFileSync(p, 'utf8'));
    } catch {
      j = {};
    }
  }
  j.context = Object.assign({}, j.context, {
    fileName: ['AGENTS.md', 'GEMINI.md'],
  });
  writeFileSync(p, JSON.stringify(j, null, 2) + '\n');
  return '.gemini/settings.json';
}

function antigravityRulesIndex(agents) {
  const lines = agents
    .map(
      (a) =>
        `- **${a.id}**: ${a.description}\n  - Allow: \`${(a.allow_globs || []).slice(0, 4).join('`, `')}\``,
    )
    .join('\n');
  return `---
trigger: always_on
description: Host-native specialists map for Antigravity (see specialists.json).
---

# Specialists (host-native)

SSOT: \`.agents/specialists.json\` · compile: \`npm run host:compile\`

Skills SSOT: \`.agents/skills/\` (Agent Skills standard).

${lines}

Always obey root **AGENTS.md**. Never bulk-load wiki/GRAPH_REPORT.
`;
}

function main() {
  const specs = loadSpecs();
  const agents = specs.agents || [];
  const out = [];

  // Clean stale Claude agents not in specs
  const claudeAgentsDir = join(ROOT, '.claude', 'agents');
  ensureDir(claudeAgentsDir);
  const want = new Set(agents.map((a) => a.id));
  if (existsSync(claudeAgentsDir)) {
    for (const f of readdirSync(claudeAgentsDir)) {
      if (!f.endsWith('.md')) continue;
      const id = f.replace(/\.md$/, '');
      if (!want.has(id)) {
        rmSync(join(claudeAgentsDir, f), { force: true });
        out.push({ removed: `.claude/agents/${f}` });
      }
    }
  }

  for (const a of agents) {
    out.push({ wrote: write(`.claude/agents/${a.id}.md`, claudeAgent(a)) });
    out.push({ wrote: write(`.cursor/agents/${a.id}.md`, cursorAgent(a)) });
    out.push({ wrote: write(`.cursor/rules/specialist-${a.id}.mdc`, cursorRule(a)) });
    out.push({ wrote: write(`.codex/agents/${a.id}.toml`, codexToml(a)) });
  }

  // Cursor always-on pointer rule
  out.push({
    wrote: write(
      '.cursor/rules/00-agents-kernel.mdc',
      `---
description: Portable kernel — always
alwaysApply: true
---

# Kernel

Follow repository root **AGENTS.md**.

- Goal-driven: \`npm run goal\` / \`npm run goal:verify\`
- Graph first: \`npm run graph:query -- "<task>"\`
- No wiki/GRAPH_REPORT dumps; no emojis in src; root-cause only
- Specialists: \`.agents/specialists.json\` (compiled host agents)
`,
    ),
  });

  // Antigravity specialists map
  out.push({
    wrote: write('.agents/rules/specialists.md', antigravityRulesIndex(agents)),
  });

  // Copilot
  out.push({ wrote: write('.github/copilot-instructions.md', copilotInstructions()) });

  // Skills mirror
  const mirrored = mirrorSkills();
  out.push({ skills_mirrored: mirrored.length, targets: ['.claude/skills', '.github/skills'] });

  // Gemini
  out.push({ wrote: geminiSettingsPatch() });

  // GEMINI.md thin
  out.push({
    wrote: write(
      'GEMINI.md',
      `# GEMINI.md — Antigravity / Gemini CLI

Follow root **[AGENTS.md](AGENTS.md)**.

Specialists: \`.agents/specialists.json\` (compiled to host agents).  
Skills: \`.agents/skills/\`  
Rules: \`.agents/rules/\`

\`\`\`bash
npm run lint && npm test
npm run graph:query -- "<task>"
npm run goal:verify
npm run host:compile
\`\`\`
`,
    ),
  });

  // CLAUDE.md thin import
  out.push({
    wrote: write(
      'CLAUDE.md',
      `@AGENTS.md

## Claude Code

- Subagents: \`.claude/agents/\` (from \`npm run host:compile\`)
- Skills: \`.claude/skills/\` (mirrored from \`.agents/skills\`)
- Ship loop: skill \`ship-loop\` or \`npm run goal -- start "…"\`
- Never bulk-read graphify wiki / GRAPH_REPORT.md
`,
    ),
  });

  // Remove custom domains tree (non-native)
  if (cleanDomains) {
    const dom = join(ROOT, '.agents', 'domains');
    if (existsSync(dom)) {
      rmSync(dom, { recursive: true, force: true });
      out.push({ removed: '.agents/domains' });
    }
  }

  // Manifest pointer
  out.push({
    wrote: write(
      '.agents/HOST_NATIVE.md',
      `# Host-native layout

| Host | Always-on | Specialists | Skills |
|------|-----------|-------------|--------|
| **All** | \`AGENTS.md\` | \`.agents/specialists.json\` | \`.agents/skills/\` |
| **Claude** | \`CLAUDE.md\` → @AGENTS.md | \`.claude/agents/*.md\` | \`.claude/skills/\` |
| **Cursor** | \`.cursor/rules/00-agents-kernel.mdc\` | \`.cursor/agents/\` + \`specialist-*.mdc\` | via AGENTS + skills path |
| **Codex** | \`AGENTS.md\` | \`.codex/agents/*.toml\` | \`.agents/skills/\` |
| **Antigravity** | \`GEMINI.md\` + \`.agents/rules/\` | rules/specialists.md | \`.agents/skills/\` |
| **Copilot** | \`.github/copilot-instructions.md\` | (path rules) | \`.github/skills/\` |
| **Gemini CLI** | \`context.fileName\`: AGENTS + GEMINI | same | \`.agents/skills/\` |

Compile: \`npm run host:compile\`  
Clean custom domains: \`npm run host:compile -- --clean-domains\`
`,
    ),
  });

  console.log(
    JSON.stringify(
      {
        ok: true,
        specialists: agents.length,
        clean_domains: cleanDomains,
        out,
      },
      null,
      2,
    ),
  );
}

main();
